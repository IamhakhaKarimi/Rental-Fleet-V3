# Additional languages live in their own auto-generated modules. Merge their UI
# dicts in here so t()/t_lang() resolve all six languages from one table.
from config.lang_de import UI as _DE_UI   # noqa: E402
from config.lang_it import UI as _IT_UI   # noqa: E402
from config.lang_es import UI as _ES_UI   # noqa: E402
from config.lang_sq import UI as _SQ_UI   # noqa: E402
TRANSLATIONS["de"] = _DE_UI
TRANSLATIONS["it"] = _IT_UI
TRANSLATIONS["es"] = _ES_UI
TRANSLATIONS["sq"] = _SQ_UI


def init_lang():
    if "lang" not in st.session_state:
        st.session_state.lang = DEFAULT_LANG


def t(key: str) -> str:
    lang = st.session_state.get("lang", DEFAULT_LANG)
    return TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS["en"].get(key, key))


def t_lang(key: str, lang: str) -> str:
    """Translate into an explicit language (used by the invoice, whose language is
    chosen at registration and is independent of the UI language)."""
    return TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS["en"].get(key, key))


def set_lang(lang: str):
    st.session_state.lang = lang
